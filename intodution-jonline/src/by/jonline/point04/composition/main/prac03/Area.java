package by.jonline.point04.composition.main.prac03;

public class Area {

	private String name;
	private int square;
	
	public Area(String name, int square) {
		
		this.name = name;
		this.square = square;
	}
	
	public String getName() {
		
		return name;
	}
	
	public int getSquare() {
		
		return square;
	}
}
